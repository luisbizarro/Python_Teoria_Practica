# operadores de asignación

a = 1
print(a)

a = a + 10
print(a)


a = 2

#suma por asignación
a += 10
print(a)

#resta por asignación
a -= 10
print(a)

#multiplicación por asignación
a *= 10
print(a)

#división por asignación
a /= 10
print(a)

#potencia por asignación
a **= 10
print(a)

#modulo por asignación (residuo de la división)
a %= 10
print(a)
