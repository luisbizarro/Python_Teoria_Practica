'''
> : mayor que
< : menor que
>= : mayor o igual que
<= : menor o igual que
== : igual que ()
!= : diferente que

'''
a = 5
b = 6
c = 7
print(a,b,c)
#al imprimir el resultado de la operacion, se obtiene un valor booleano (osea True o False)
resultado = a > b
print(resultado)

resultado = a < b
print(resultado)

resultado = a >= b
print(resultado)

resultado = a <= b
print(resultado)

resultado = a == b
print(resultado)

resultado = a != b
print(resultado)

resultado = (a+c) > b
print(resultado)

